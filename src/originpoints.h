//*****************************************************************************
//This class contains an enumerated type describing the different origin points
// available to each element. For example, a window could be decribed relative
// to the bottom left (default) or any of the other corners as required.
//
//Written by Samuel Kinnett, 2015
//*****************************************************************************

#ifndef ORIGIN_POINTS_H
#define ORIGIN_POINTS_H

enum originPoints {
	bottomLeft,
	bottomRight,
	topLeft,
	topRight
};

#endif
