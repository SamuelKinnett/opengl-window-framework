//*****************************************************************************
//This class contains an enumerated type describing the different anchor points
// available to each element. For example, a window could be anchored to the
// bottom left (default) or to the top right.
//
//Written by Samuel Kinnett, 2015
//*****************************************************************************

#ifndef ANCHOR_POINTS_H
#define ANCHOR_POINTS_H

enum anchorPoints {
	bottomLeft,
	bottomRight,
	topLeft,
	topRight
};

#endif
